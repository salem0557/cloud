"""Technical indicators computed on the underlying's price history."""

from typing import List, Optional


def compute_rsi(closes: List[float], period: int = 14) -> Optional[float]:
    """Wilder's RSI (the standard/default RSI most platforms use).

    Returns None if there isn't enough price history (need at least
    `period + 1` closes) to compute a value.
    """
    if len(closes) < period + 1:
        return None

    deltas = [closes[i] - closes[i - 1] for i in range(1, len(closes))]
    gains = [d if d > 0 else 0.0 for d in deltas]
    losses = [-d if d < 0 else 0.0 for d in deltas]

    avg_gain = sum(gains[:period]) / period
    avg_loss = sum(losses[:period]) / period

    for i in range(period, len(deltas)):
        avg_gain = (avg_gain * (period - 1) + gains[i]) / period
        avg_loss = (avg_loss * (period - 1) + losses[i]) / period

    if avg_loss == 0:
        return 100.0
    rs = avg_gain / avg_loss
    return 100.0 - (100.0 / (1.0 + rs))
